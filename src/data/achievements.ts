import type { Word, UserStats } from '@/types';
import { boxOf } from '@/data/nebulae';

export interface AchieveContext {
  words: Word[];
  stats: UserStats;
  level: number;
}

export type I18nStr = { en: string; ja: string; zh?: string; ko?: string };

export interface AchievementDef {
  id: string;
  emoji: string;
  name: I18nStr;
  desc: I18nStr;
  category: 'vocab' | 'quiz' | 'accuracy' | 'streak' | 'level' | 'special';
  type: 'single' | 'progress';
  max: number;
  check: (ctx: AchieveContext) => { unlocked: boolean; current: number };
}

const tagCount  = (words: Word[]) => { const s = new Set<string>(); words.forEach(w => w.tags?.forEach(t => s.add(t))); return s.size; };
const n7Count   = (words: Word[]) => words.filter(w => boxOf(w.accuracy) === 7).length;
const perfCount = (words: Word[], minAtt = 5) => words.filter(w => w.accuracy >= 100 && w.attempts >= minAtt).length;
const highCount = (words: Word[], pct = 80, minAtt = 3) => words.filter(w => w.accuracy >= pct && w.attempts >= minAtt).length;
const avgAtt    = (words: Word[]) => words.length > 0 ? words.reduce((s, w) => s + w.attempts, 0) / words.length : 0;
const nebCount  = (words: Word[], n: number) => words.filter(w => boxOf(w.accuracy) === n).length;

